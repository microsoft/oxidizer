//! The "tonic bridge" modality: wiring a `tonic`-generated gRPC server into a
//! REST API.
//!
//! This is the common case: you implement your service only against `tonic`'s
//! generated server trait, and `rest_over_grpc::build` emits a blanket `impl`
//! that makes it a `rest_over_grpc` service too, so one implementation serves
//! both gRPC and REST. (To hand-write the service trait instead, see the
//! [`custom`](crate::custom) module — it serves the *same* `library.proto`
//! service the other way, so the two modules are an apples-to-apples comparison.)
//!
//! [`LibraryService`] is the tonic-only handler shared by the runnable examples
//! that exercise this modality.

/// The generated library service: tonic messages + server trait, pbjson serde,
/// and the `rest_over_grpc` REST trait + transcoder + tonic bridge.
pub mod library {
    #![allow(
        clippy::all,
        clippy::pedantic,
        clippy::nursery,
        clippy::restriction,
        missing_docs,
        rustdoc::all,
        reason = "code generated by tonic + pbjson + rest_over_grpc::build"
    )]

    include!(concat!(env!("OUT_DIR"), "/tonic_bridge/library.rs"));
    include!(concat!(env!("OUT_DIR"), "/tonic_bridge/library.serde.rs"));
    include!(concat!(env!("OUT_DIR"), "/tonic_bridge/library.rest.rs"));
}

